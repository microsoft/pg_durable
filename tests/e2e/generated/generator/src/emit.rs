//! Emits the two committed-or-generated artifacts from a rendered shape:
//! a self-contained `.sql` E2E test (gitignored, regenerated on demand) and an
//! entry in the golden `manifest.json` (committed regression baseline).

use crate::shape::Comb;
use std::collections::BTreeMap;

/// One fully-resolved shape ready to emit.
pub struct ShapeRecord {
    pub id: String,
    pub signature: String,
    pub depth: u32,
    /// `"live"` for shapes expected to pass, `"quarantine"` for shapes a known
    /// product loop-nesting defect (#227/#230) makes fail.
    pub class: &'static str,
    /// The structural reason a shape is quarantined, or `None` when it is live.
    pub reason: Option<&'static str>,
    pub dsl: String,
    pub expected: BTreeMap<String, u64>,
    /// Phase 5 (#232): the reference interpreter's happens-before (`≺`) edges as
    /// `((earlier_path, earlier_iter), (later_path, later_iter))` pairs. Live
    /// `.sql` tests assert each pair as `earlier.wall_clock < later.wall_clock`;
    /// the manifest records them as the committed causal-order golden.
    pub ordered_pairs: Vec<((String, u64), (String, u64))>,
}

/// Parameters that describe how a matrix was generated (manifest header).
pub struct MatrixMeta<'a> {
    pub max_depth: u32,
    pub combinators: &'a [Comb],
    pub loop_iters: u64,
    pub include_seeds: bool,
}

/// Renders the full text of a self-contained `.sql` E2E test for one shape.
///
/// The test starts the durable instance, waits for completion, asserts the
/// Phase 1 structural-invariant oracle passes, asserts the exact per-path
/// execution counts, asserts no unexpected node paths appear, and — for live
/// shapes — asserts the Phase 5 causal-order oracle: every happens-before edge
/// the reference interpreter derived holds as `earlier.wall_clock < later`.
pub fn sql_test(rec: &ShapeRecord, wait_timeout: u32) -> String {
    let id = &rec.id;
    // Phase 5 (#232): only live shapes get the causal-order block — quarantined
    // shapes already fail their COUNT assertions, and an empty edge set (a lone
    // marker, or concurrent-only join) has nothing to assert.
    let emit_order = rec.reason.is_none() && !rec.ordered_pairs.is_empty();
    let mut out = String::new();

    out.push_str("-- Copyright (c) Microsoft Corporation.\n");
    out.push_str("-- Licensed under the PostgreSQL License.\n");
    out.push_str("--\n");
    out.push_str("-- AUTO-GENERATED by pg_durable_matrix_gen — DO NOT EDIT BY HAND.\n");
    out.push_str(
        "-- Regenerate: cargo run --manifest-path tests/e2e/generated/generator/Cargo.toml\n",
    );
    out.push_str(&format!(
        "-- Shape {id}  signature={}  depth={}\n",
        rec.signature, rec.depth
    ));
    if let Some(reason) = rec.reason {
        out.push_str(&format!(
            "-- QUARANTINED ({reason}): a known product loop-nesting defect (#227/#230)\n\
             -- makes this shape fail today. The assertions below encode the CORRECT\n\
             -- expected behavior; this test is expected to start passing once the\n\
             -- product bug is fixed, at which point it should be promoted to live.\n"
        ));
    }
    out.push('\n');

    out.push_str("SET SESSION AUTHORIZATION df_e2e_user;\n\n");
    out.push_str(
        "CREATE TABLE IF NOT EXISTS df_gen_trace (shape_id TEXT, node_path TEXT, iteration INT, \
wall_clock TIMESTAMPTZ);\n",
    );
    // A persistent test database may still hold a pre-isolation df_gen_trace
    // (no shape_id column); add it idempotently so old and fresh runs agree.
    out.push_str("ALTER TABLE df_gen_trace ADD COLUMN IF NOT EXISTS shape_id TEXT;\n");
    out.push_str(&format!(
        "DELETE FROM df_gen_trace WHERE shape_id = '{id}';\n\n"
    ));

    out.push_str("CREATE TEMP TABLE _gen_state (instance_id TEXT);\n");
    out.push_str("INSERT INTO _gen_state SELECT df.start(\n    ");
    out.push_str(&rec.dsl);
    out.push_str(&format!(",\n    '{id}'\n);\n\n"));

    out.push_str("DO $GEN$\n");
    out.push_str("DECLARE\n");
    out.push_str("    inst_id TEXT;\n");
    out.push_str("    status TEXT;\n");
    out.push_str("    all_passed BOOLEAN;\n");
    out.push_str("    viol TEXT;\n");
    out.push_str("    unexpected TEXT;\n");
    if emit_order {
        out.push_str("    ord_viol TEXT;\n");
    }
    out.push_str("BEGIN\n");
    out.push_str("    SELECT instance_id INTO inst_id FROM _gen_state;\n");
    out.push_str(&format!(
        "    SELECT df.wait_for_completion(inst_id, {wait_timeout}) INTO status;\n"
    ));
    out.push_str("    IF status != 'completed' THEN\n");
    out.push_str(&format!(
        "        RAISE EXCEPTION 'TEST FAILED [{id}]: status = %', status;\n"
    ));
    out.push_str("    END IF;\n\n");

    out.push_str("    -- Phase 1 structural-invariant oracle must pass.\n");
    out.push_str(
        "    SELECT COALESCE(bool_and(passed), false), \
string_agg(invariant, ', ') FILTER (WHERE NOT passed)\n",
    );
    out.push_str("      INTO all_passed, viol\n");
    out.push_str("      FROM df.assert_structural_invariants(inst_id);\n");
    out.push_str("    IF NOT all_passed THEN\n");
    out.push_str(&format!(
        "        RAISE EXCEPTION 'TEST FAILED [{id}]: invariant violation(s): %', viol;\n"
    ));
    out.push_str("    END IF;\n\n");

    out.push_str("    -- Per-path execution counts must match the generator ground truth.\n");
    for (path, count) in &rec.expected {
        out.push_str(&format!(
            "    IF (SELECT COUNT(*) FROM df_gen_trace \
WHERE shape_id = '{id}' AND node_path = '{path}') <> {count} THEN\n"
        ));
        out.push_str(&format!(
            "        RAISE EXCEPTION 'TEST FAILED [{id}]: path {path} expected {count}, got %',\n"
        ));
        out.push_str(&format!(
            "            (SELECT COUNT(*) FROM df_gen_trace \
WHERE shape_id = '{id}' AND node_path = '{path}');\n"
        ));
        out.push_str("    END IF;\n");
    }
    out.push('\n');

    out.push_str("    -- No unexpected node paths may appear in the trace.\n");
    if rec.expected.is_empty() {
        // An empty expected-set would make `NOT IN ()` invalid SQL; any trace
        // row at all is unexpected, so guard on the shape scope alone.
        out.push_str("    SELECT string_agg(DISTINCT node_path, ', ') INTO unexpected\n");
        out.push_str("      FROM df_gen_trace\n");
        out.push_str(&format!("     WHERE shape_id = '{id}';\n"));
    } else {
        let known: Vec<String> = rec.expected.keys().map(|p| format!("'{p}'")).collect();
        out.push_str("    SELECT string_agg(DISTINCT node_path, ', ') INTO unexpected\n");
        out.push_str("      FROM df_gen_trace\n");
        out.push_str(&format!(
            "     WHERE shape_id = '{id}' AND node_path NOT IN ({});\n",
            known.join(", ")
        ));
    }
    out.push_str("    IF unexpected IS NOT NULL THEN\n");
    out.push_str(&format!(
        "        RAISE EXCEPTION 'TEST FAILED [{id}]: unexpected path(s): %', unexpected;\n"
    ));
    out.push_str("    END IF;\n");

    if emit_order {
        // Phase 5 (#232) causal-order oracle. Each VALUES row is one happens-
        // before (≺) edge from the reference interpreter; the two LEFT JOINs
        // resolve both endpoints to their trace rows and the WHERE keeps every
        // edge the runtime VIOLATED. An edge is a violation when EITHER endpoint
        // never executed at its (path, iteration) — the LEFT JOIN leaves its
        // wall_clock NULL — OR the earlier marker fired at-or-after the later
        // one. The LEFT (not INNER) JOIN is deliberate and fail-closed: an INNER
        // JOIN silently DROPS an edge whose endpoint row is missing, so a dropped
        // or duplicated iteration could pass unflagged; treating a missing
        // endpoint as the violation means the oracle never trusts the marker's
        // own MAX(iteration)+1 numbering (the very thing under test). Markers run
        // in distinct duroxide activities milliseconds apart, so an honored edge
        // always shows a strict clock_timestamp() gap. CONCURRENT siblings
        // (join/race branches) carry no edge, so this can never flake on them.
        out.push('\n');
        out.push_str("    -- Phase 1+2 counts pin WHAT ran; this pins the ORDER it ran in.\n");
        out.push_str("    SELECT string_agg(\n");
        out.push_str("             CASE WHEN tu.wall_clock IS NULL OR tv.wall_clock IS NULL\n");
        out.push_str("                  THEN v.descr || ' (missing endpoint)' ELSE v.descr END,\n");
        out.push_str("             ', ' ORDER BY v.descr)\n");
        out.push_str("      INTO ord_viol\n");
        out.push_str("      FROM (VALUES\n");
        let n = rec.ordered_pairs.len();
        for (i, ((pu, iu), (pv, iv))) in rec.ordered_pairs.iter().enumerate() {
            let sep = if i + 1 < n { "," } else { "" };
            out.push_str(&format!(
                "        ('{pu}', {iu}, '{pv}', {iv}, '{pu}#{iu} -> {pv}#{iv}'){sep}\n"
            ));
        }
        out.push_str("      ) AS v(pu, iu, pv, iv, descr)\n");
        out.push_str(&format!(
            "      LEFT JOIN df_gen_trace tu ON tu.shape_id = '{id}' \
AND tu.node_path = v.pu AND tu.iteration = v.iu\n"
        ));
        out.push_str(&format!(
            "      LEFT JOIN df_gen_trace tv ON tv.shape_id = '{id}' \
AND tv.node_path = v.pv AND tv.iteration = v.iv\n"
        ));
        out.push_str(
            "     WHERE tu.wall_clock IS NULL OR tv.wall_clock IS NULL \
OR tu.wall_clock >= tv.wall_clock;\n",
        );
        out.push_str("    IF ord_viol IS NOT NULL THEN\n");
        out.push_str(&format!(
            "        RAISE EXCEPTION 'TEST FAILED [{id}]: causal-order violation(s): %', ord_viol;\n"
        ));
        out.push_str("    END IF;\n");
    }

    out.push_str("END $GEN$;\n\n");
    out.push_str("DROP TABLE _gen_state;\n");
    out.push_str("SELECT 'TEST PASSED' AS result;\n");

    out
}

pub(crate) fn json_escape(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 2);
    for c in s.chars() {
        match c {
            '"' => out.push_str("\\\""),
            '\\' => out.push_str("\\\\"),
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            '\t' => out.push_str("\\t"),
            c if (c as u32) < 0x20 => out.push_str(&format!("\\u{:04x}", c as u32)),
            c => out.push(c),
        }
    }
    out
}

/// Serializes the full matrix to the golden `manifest.json` (deterministic,
/// 2-space indented, sorted keys).
pub fn manifest_json(records: &[ShapeRecord], meta: &MatrixMeta) -> String {
    let mut out = String::new();
    out.push_str("{\n");
    out.push_str("  \"version\": 1,\n");
    out.push_str("  \"generator\": \"pg_durable_matrix_gen\",\n");
    out.push_str(&format!("  \"max_depth\": {},\n", meta.max_depth));
    let combs: Vec<String> = meta
        .combinators
        .iter()
        .map(|c| format!("\"{}\"", c.name()))
        .collect();
    out.push_str(&format!("  \"combinators\": [{}],\n", combs.join(", ")));
    out.push_str(&format!("  \"loop_iters\": {},\n", meta.loop_iters));
    out.push_str(&format!("  \"include_seeds\": {},\n", meta.include_seeds));
    out.push_str(&format!("  \"shape_count\": {},\n", records.len()));
    out.push_str("  \"shapes\": [\n");

    for (i, rec) in records.iter().enumerate() {
        out.push_str("    {\n");
        out.push_str(&format!("      \"id\": \"{}\",\n", rec.id));
        out.push_str(&format!(
            "      \"signature\": \"{}\",\n",
            json_escape(&rec.signature)
        ));
        out.push_str(&format!("      \"depth\": {},\n", rec.depth));
        out.push_str(&format!("      \"class\": \"{}\",\n", rec.class));
        match rec.reason {
            Some(reason) => {
                out.push_str(&format!("      \"reason\": \"{}\",\n", json_escape(reason)))
            }
            None => out.push_str("      \"reason\": null,\n"),
        }
        out.push_str(&format!("      \"dsl\": \"{}\",\n", json_escape(&rec.dsl)));

        if rec.expected.is_empty() {
            out.push_str("      \"expected\": {},\n");
        } else {
            out.push_str("      \"expected\": {\n");
            let entries: Vec<String> = rec
                .expected
                .iter()
                .map(|(p, c)| format!("        \"{}\": {}", json_escape(p), c))
                .collect();
            out.push_str(&entries.join(",\n"));
            out.push_str("\n      },\n");
        }

        // Phase 5 (#232): the committed causal-order golden — the reference
        // interpreter's happens-before edges as `["earlier_path", iter,
        // "later_path", iter]`. Present for every shape (the interpreter derives
        // order regardless of class); only the live `.sql` emission is gated.
        if rec.ordered_pairs.is_empty() {
            out.push_str("      \"order\": []\n");
        } else {
            out.push_str("      \"order\": [\n");
            let entries: Vec<String> = rec
                .ordered_pairs
                .iter()
                .map(|((pu, iu), (pv, iv))| {
                    format!(
                        "        [\"{}\", {}, \"{}\", {}]",
                        json_escape(pu),
                        iu,
                        json_escape(pv),
                        iv
                    )
                })
                .collect();
            out.push_str(&entries.join(",\n"));
            out.push_str("\n      ]\n");
        }

        if i + 1 < records.len() {
            out.push_str("    },\n");
        } else {
            out.push_str("    }\n");
        }
    }

    out.push_str("  ]\n");
    out.push_str("}\n");
    out
}

#[cfg(test)]
mod tests {
    use super::*;

    fn sample() -> ShapeRecord {
        let mut expected = BTreeMap::new();
        expected.insert("r.0".to_string(), 1u64);
        expected.insert("r.1".to_string(), 1u64);
        ShapeRecord {
            id: "gen-0001".to_string(),
            signature: "S(M,M)".to_string(),
            depth: 1,
            class: "live",
            reason: None,
            dsl: "df.seq(df.sql($mk$...$mk$), df.sql($mk$...$mk$))".to_string(),
            expected,
            ordered_pairs: vec![(("r.0".to_string(), 1), ("r.1".to_string(), 1))],
        }
    }

    fn quarantined_sample() -> ShapeRecord {
        let mut expected = BTreeMap::new();
        expected.insert("r.1.b".to_string(), 2u64);
        ShapeRecord {
            id: "gen-0099".to_string(),
            signature: "J(M,L(M))".to_string(),
            depth: 2,
            class: "quarantine",
            reason: Some("loop-in-join"),
            dsl: "df.join(df.sql($mk$...$mk$), df.loop(df.sql($mk$...$mk$)))".to_string(),
            expected,
            ordered_pairs: vec![(("r.1.b".to_string(), 1), ("r.1.b".to_string(), 2))],
        }
    }

    #[test]
    fn sql_test_has_passed_sentinel_and_authorization() {
        let t = sql_test(&sample(), 60);
        assert!(t.contains("SET SESSION AUTHORIZATION df_e2e_user;"));
        assert!(t.trim_end().ends_with("SELECT 'TEST PASSED' AS result;"));
        assert!(t.contains("df.assert_structural_invariants(inst_id)"));
        assert!(t.contains("df.wait_for_completion(inst_id, 60)"));
        // The oracle aggregate must COALESCE so a zero-row result fails loudly
        // instead of NULL → silent pass.
        assert!(t.contains("COALESCE(bool_and(passed), false)"));
    }

    #[test]
    fn sql_test_asserts_each_path() {
        let t = sql_test(&sample(), 60);
        assert!(t.contains("AND node_path = 'r.0'"));
        assert!(t.contains("AND node_path = 'r.1'"));
        // Each expected count (here 1) is asserted with a `<> N THEN` guard.
        assert!(t.contains("<> 1 THEN"));
        assert!(t.contains("NOT IN ('r.0', 'r.1')"));
    }

    #[test]
    fn sql_test_empty_expected_avoids_invalid_not_in() {
        let mut rec = sample();
        rec.expected = BTreeMap::new();
        let t = sql_test(&rec, 60);
        // An empty expected-set must not emit invalid `NOT IN ()`.
        assert!(!t.contains("NOT IN ()"));
        // It falls back to a shape-scoped any-row guard.
        assert!(t.contains("WHERE shape_id = 'gen-0001';"));
    }

    #[test]
    fn sql_test_scopes_trace_by_shape_id() {
        let t = sql_test(&sample(), 60);
        // Table carries shape_id; the per-test reset and the unexpected-path
        // guard must both be scoped to this shape so a foreign zombie instance
        // cannot fail an innocent shape.
        assert!(t.contains("df_gen_trace (shape_id TEXT, node_path TEXT, iteration INT"));
        assert!(t.contains("DELETE FROM df_gen_trace WHERE shape_id = 'gen-0001';"));
        assert!(t.contains("WHERE shape_id = 'gen-0001' AND node_path NOT IN"));
    }

    #[test]
    fn manifest_is_deterministic() {
        let meta = MatrixMeta {
            max_depth: 2,
            combinators: &[Comb::Seq],
            loop_iters: 2,
            include_seeds: false,
        };
        let a = manifest_json(&[sample()], &meta);
        let b = manifest_json(&[sample()], &meta);
        assert_eq!(a, b);
        assert!(a.contains("\"shape_count\": 1"));
        assert!(a.contains("\"id\": \"gen-0001\""));
    }

    #[test]
    fn manifest_carries_class_and_reason() {
        let meta = MatrixMeta {
            max_depth: 2,
            combinators: &[Comb::Seq, Comb::Join, Comb::Loop],
            loop_iters: 2,
            include_seeds: false,
        };
        let m = manifest_json(&[sample(), quarantined_sample()], &meta);
        assert!(m.contains("\"class\": \"live\""));
        assert!(m.contains("\"reason\": null"));
        assert!(m.contains("\"class\": \"quarantine\""));
        assert!(m.contains("\"reason\": \"loop-in-join\""));
    }

    #[test]
    fn quarantined_test_carries_explanatory_header() {
        let t = sql_test(&quarantined_sample(), 10);
        assert!(t.contains("QUARANTINED (loop-in-join)"));
        // Even quarantined, the assertions still encode correct behavior.
        assert!(t.contains("df.assert_structural_invariants(inst_id)"));
        assert!(t.trim_end().ends_with("SELECT 'TEST PASSED' AS result;"));
    }

    #[test]
    fn sql_test_emits_causal_order_block_for_live_shape() {
        let t = sql_test(&sample(), 60);
        // Live shape with edges: the order oracle declares its var, emits the
        // edge as a VALUES row, joins both endpoints, and raises on violation.
        assert!(t.contains("    ord_viol TEXT;\n"));
        assert!(t.contains("('r.0', 1, 'r.1', 1, 'r.0#1 -> r.1#1')"));
        // Hardened (fail-closed): LEFT JOINs make a missing endpoint a violation
        // in its own right, so a dropped/duplicated iteration cannot slip past an
        // INNER JOIN that would silently drop the unresolved edge.
        assert!(t.contains("LEFT JOIN df_gen_trace tu ON"));
        assert!(t.contains("LEFT JOIN df_gen_trace tv ON"));
        assert!(t.contains(
            "WHERE tu.wall_clock IS NULL OR tv.wall_clock IS NULL OR tu.wall_clock >= tv.wall_clock;"
        ));
        assert!(t.contains("(missing endpoint)"));
        assert!(t.contains("causal-order violation(s)"));
        // The order ORACLE BLOCK is the LAST assertion, after the
        // unexpected-path guard (the `ord_viol TEXT` declare precedes both).
        let order_at = t.find("causal-order violation").unwrap();
        let unexpected_at = t.find("unexpected path(s)").unwrap();
        assert!(order_at > unexpected_at);
    }

    #[test]
    fn sql_test_omits_causal_order_block_when_quarantined() {
        // A quarantined shape fails its COUNT assertions first; emitting an
        // order oracle on a known-buggy trace would be noise.
        let t = sql_test(&quarantined_sample(), 10);
        assert!(!t.contains("ord_viol"));
        assert!(!t.contains("causal-order violation"));
    }

    #[test]
    fn sql_test_omits_causal_order_block_when_no_edges() {
        // A lone marker (or a purely-concurrent join) has no happens-before
        // edge, so there is nothing to assert.
        let mut rec = sample();
        rec.ordered_pairs = Vec::new();
        let t = sql_test(&rec, 60);
        assert!(!t.contains("ord_viol"));
    }

    #[test]
    fn manifest_carries_order_field() {
        let meta = MatrixMeta {
            max_depth: 2,
            combinators: &[Comb::Seq],
            loop_iters: 2,
            include_seeds: false,
        };
        let m = manifest_json(&[sample()], &meta);
        assert!(m.contains("\"order\": [\n"));
        assert!(m.contains("[\"r.0\", 1, \"r.1\", 1]"));
        // An edge-free shape records an empty array, never omits the key.
        let mut bare = sample();
        bare.ordered_pairs = Vec::new();
        let m2 = manifest_json(&[bare], &meta);
        assert!(m2.contains("\"order\": []\n"));
    }
}
